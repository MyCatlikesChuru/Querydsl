    @Test
    public void subQuery() {

        QMember membeSub = new QMember("membeSub");

        List<Member> result = queryFactory
                .selectFrom(member)
                .where(member.age.eq(
                        JPAExpressions
                                .select(membeSub.age.max())
                                .from(membeSub)
                ))
                .fetch();

        assertThat(result)
                .extracting("age")
                .containsExactly(40);
    }

    @Test
    public void subQueryIn() {

        QMember membeSub = new QMember("membeSub");

        List<Member> result = queryFactory
                .selectFrom(member)
                .where(member.age.in(
                        JPAExpressions
                                .select(membeSub.age)
                                .from(membeSub)
                                .where(membeSub.age.gt(10))
                ))
                .fetch();

        System.out.println("사이즈가 궁금해~ = " + result.size());

        assertThat(result)
                .extracting("age")
                .containsExactly(20,30,40);
    }

